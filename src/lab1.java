    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        try {
            double a= Double.parseDouble(jTextField1.getText());
            double b= Double.parseDouble(jTextField2.getText());
            double n= Double.parseDouble(jTextField3.getText());

            Chislo chislo;
            try {
                chislo = new Chislo(a,b,n);
                Object[] rowData = {a,b,n,""}; // Данные для добавления в таблицу

                // Вставить данные в первую строку таблицы
                model.insertRow(0, rowData);

                // Добавление объекта Chislo в коллекцию на первую позицию
                RecIntegral.add(0, chislo);
        } catch (InvalidRecIntegralValueException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Input Error", JOptionPane.ERROR_MESSAGE);
        }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter valid numeric values.", "Input Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

