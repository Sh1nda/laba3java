    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int row = 0; // использовать первую строку таблицы

        double a= Double.parseDouble(model.getValueAt(row, 0).toString());
        double b= Double.parseDouble(model.getValueAt(row, 1).toString());
        double n= Double.parseDouble(model.getValueAt(row, 2).toString());
        double i = 0;
        double result = 0;
  for(i = a; i < b; i+=n){
            if((i + n) < b) {
                result += ((Math.cos(i*i) + Math.cos(Math.pow(n +i, 2))) * (n/2));
            }
            else{
                result += (Math.cos(i*i) + Math.cos(b)) * ((b - i) / 2);
            }
        }
        model.setValueAt(result, row, 3); // Устанавливаем значение результата в четвертый столбец таблицы
    }//GEN-LAST:event_jButton3ActionPerformed

    
