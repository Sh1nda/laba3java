    public class InvalidRecIntegralValueException extends Exception {
        public InvalidRecIntegralValueException(String message) {
            super(message);
        }
    }
        
    public class Chislo {
        private double a;
        private double b;
        private double n;

        public Chislo() {
            // Конструктор по умолчанию
        }
        
        public Chislo(double a, double b, double step) throws InvalidRecIntegralValueException {
            if (a < 0.000001 || b > 1000000 ||
                a < 0.000001 || b > 1000000 ||
                a < b || step > a - b||
                step < 0.000001 || step > 1000000) {
                throw new InvalidRecIntegralValueException("Ошибка. Попробуйте еще раз");
            }

            this.a = a;
            this.b = b;
            this.n = step;
        }
        
        public void setLowerBound(double lowerBound) {
            this.a = a;
        }

        public void setUpperBound(double upperBound) {
            this.b= b;
        }

        public void setStep(double step) {
            this.n= n;
        }

        @Override
        public String toString() {
            return "Chislo [lowerBound=" + a + ", upperBound=" + b + ", step=" + n + "]";
        }
    }
    
